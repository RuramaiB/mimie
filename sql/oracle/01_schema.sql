-- Entity 1: stands
CREATE TABLE stands (
    stand_number VARCHAR2(20) NOT NULL,
    location VARCHAR2(200) NOT NULL,
    size_m2 NUMBER(12,2) NOT NULL,
    activity VARCHAR2(50) NOT NULL,
    picture_url VARCHAR2(500),
    gps_coordinates VARCHAR2(500) NOT NULL,
    location_city VARCHAR2(100) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT pk_stands PRIMARY KEY (stand_number),
    CONSTRAINT chk_stands_size CHECK (size_m2 > 0),
    CONSTRAINT chk_stands_activity CHECK (activity IN ('Residential', 'Commercial'))
);

-- Entity 2: stand_survey
CREATE TABLE stand_survey (
    survey_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    stand_number VARCHAR2(20) NOT NULL,
    survey_status NUMBER(1) DEFAULT 0 NOT NULL,
    province VARCHAR2(100) NOT NULL,
    district VARCHAR2(100) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_survey_stand FOREIGN KEY (stand_number) 
        REFERENCES stands (stand_number) ON DELETE CASCADE
);

-- Entity 3: stand_subdivisions
CREATE TABLE stand_subdivisions (
    subdivision_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    stand_number VARCHAR2(20) NOT NULL,
    allocation_status NUMBER(1) DEFAULT 0 NOT NULL,
    size_m2 NUMBER(10,2) NOT NULL,
    remarks CLOB,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_subdivisions_stand FOREIGN KEY (stand_number) 
        REFERENCES stands (stand_number),
    CONSTRAINT chk_subdivision_size CHECK (size_m2 > 0)
);

-- Entity 4: stand_owners
CREATE TABLE stand_owners (
    stand_owner_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    firstname VARCHAR2(100) NOT NULL,
    date_of_birth DATE NOT NULL,
    gender VARCHAR2(10) NOT NULL,
    disability_status NUMBER(1) DEFAULT 0 NOT NULL,
    province VARCHAR2(100) NOT NULL,
    district VARCHAR2(100) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT chk_owners_gender CHECK (gender IN ('Male', 'Female', 'Other'))
);

-- Entity 5: dependents
CREATE TABLE dependents (
    dependent_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    stand_owner_id NUMBER NOT NULL,
    firstname VARCHAR2(100) NOT NULL,
    date_of_birth DATE NOT NULL,
    gender VARCHAR2(10) NOT NULL,
    disability_status NUMBER(1) DEFAULT 0 NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_dependents_owner FOREIGN KEY (stand_owner_id) 
        REFERENCES stand_owners (stand_owner_id) ON DELETE CASCADE,
    CONSTRAINT chk_dependents_gender CHECK (gender IN ('Male', 'Female', 'Other'))
);

-- Entity 6: stand_allocations
CREATE TABLE stand_allocations (
    allocation_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    stand_owner_id NUMBER NOT NULL,
    subdivision_id NUMBER NOT NULL,
    date_of_allocation DATE NOT NULL,
    price_per_m2 NUMBER(10,2) NOT NULL,
    created_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT fk_allocations_owner FOREIGN KEY (stand_owner_id) 
        REFERENCES stand_owners (stand_owner_id),
    CONSTRAINT fk_allocations_subdivision FOREIGN KEY (subdivision_id) 
        REFERENCES stand_subdivisions (subdivision_id),
    CONSTRAINT chk_allocations_price CHECK (price_per_m2 > 0)
);

-- Audit Table
CREATE TABLE stand_allocations_audit (
    audit_id NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    action_type VARCHAR2(10) NOT NULL,
    allocation_id NUMBER NOT NULL,
    stand_owner_id NUMBER NOT NULL,
    subdivision_id NUMBER NOT NULL,
    date_of_allocation DATE NOT NULL,
    price_per_m2 NUMBER(10,2) NOT NULL,
    changed_by VARCHAR2(100) NOT NULL,
    changed_at TIMESTAMP DEFAULT CURRENT_TIMESTAMP
);

-- Metadata Catalog Table
CREATE TABLE metadata_catalogue (
    table_name VARCHAR2(100) NOT NULL,
    column_name VARCHAR2(100) NOT NULL,
    data_type VARCHAR2(50) NOT NULL,
    is_pii NUMBER(1) DEFAULT 0,
    data_classification VARCHAR2(50) DEFAULT 'Internal',
    data_owner VARCHAR2(100) DEFAULT 'Ministry of Lands',
    data_steward VARCHAR2(100) DEFAULT 'GIS Department',
    CONSTRAINT pk_metadata PRIMARY KEY (table_name, column_name)
);


