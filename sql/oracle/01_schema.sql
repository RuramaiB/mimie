CREATE OR REPLACE PACKAGE BODY pkg_land_stand AS

    PROCEDURE sp_allocate_stand(
        p_owner_id IN NUMBER,
        p_subdivision_id IN NUMBER,
        p_price_per_m2 IN NUMBER
    ) IS
        v_already_allocated NUMBER := 0;
        v_is_owner_valid NUMBER := 0;
        v_is_sub_valid NUMBER := 0;
    BEGIN
        -- Check Owner
        SELECT COUNT(*) INTO v_is_owner_valid FROM stand_owners WHERE stand_owner_id = p_owner_id;
        IF v_is_owner_valid = 0 THEN
            raise_application_error(-20010, 'Validation Error: Owner ID is not registered.');
        END IF;

        -- Check Subdivision
        SELECT COUNT(*) INTO v_is_sub_valid FROM stand_subdivisions WHERE subdivision_id = p_subdivision_id;
        IF v_is_sub_valid = 0 THEN
            raise_application_error(-20011, 'Validation Error: Subdivision ID does not exist.');
        END IF;

        -- Check price
        IF p_price_per_m2 <= 0 THEN
            raise_application_error(-20012, 'Validation Error: Price per m2 must be positive.');
        END IF;

        -- Check if already allocated
        SELECT allocation_status INTO v_already_allocated FROM stand_subdivisions WHERE subdivision_id = p_subdivision_id;
        IF v_already_allocated = 1 THEN
            raise_application_error(-20013, 'Business Rule Violation: This subdivision is already allocated.');
        END IF;

        -- Execute allocation
        INSERT INTO stand_allocations (stand_owner_id, subdivision_id, date_of_allocation, price_per_m2)
        VALUES (p_owner_id, p_subdivision_id, SYSDATE, p_price_per_m2);
        
        COMMIT;
    EXCEPTION
        WHEN OTHERS THEN
            ROLLBACK;
            RAISE;
    END sp_allocate_stand;

    PROCEDURE sp_owner_report(
        p_owner_id IN NUMBER,
        p_cursor OUT t_owner_report_cursor
    ) IS
    BEGIN
        OPEN p_cursor FOR
        SELECT 
            so.stand_owner_id,
            so.firstname,
            so.province,
            so.district,
            sa.allocation_id,
            sa.date_of_allocation,
            sa.price_per_m2,
            sub.subdivision_id,
            sub.size_m2,
            s.stand_number,
            s.location
        FROM stand_owners so
        LEFT JOIN stand_allocations sa ON so.stand_owner_id = sa.stand_owner_id
        LEFT JOIN stand_subdivisions sub ON sa.subdivision_id = sub.subdivision_id
        LEFT JOIN stands s ON sub.stand_number = s.stand_number
        WHERE so.stand_owner_id = p_owner_id;
    END sp_owner_report;

    PROCEDURE sp_available_subdivisions(
        p_province IN VARCHAR2,
        p_district IN VARCHAR2,
        p_cursor OUT t_sub_cursor
    ) IS
    BEGIN
        OPEN p_cursor FOR
        SELECT 
            sub.subdivision_id,
            sub.stand_number,
            sub.size_m2,
            CAST(sub.remarks AS VARCHAR2(2000)) AS remarks,
            sur.province,
            sur.district
        FROM stand_subdivisions sub
        JOIN stand_survey sur ON sub.stand_number = sur.stand_number
        WHERE sub.allocation_status = 0
          AND sur.province = p_province
          AND sur.district = p_district;
    END sp_available_subdivisions;

END pkg_land_stand;
/


