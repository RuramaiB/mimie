-- Trigger 2: Check Subdivision Size BEFORE INSERT
CREATE OR REPLACE TRIGGER trg_check_subdivision_size
BEFORE INSERT ON stand_subdivisions
FOR EACH ROW
DECLARE
    v_parent_size NUMBER(12,2);
    v_current_sum NUMBER(12,2);
    v_survey_exists NUMBER := 0;
BEGIN
    -- Business Rule: A stand must have an active survey status verified before subdividing.
    SELECT COUNT(*) INTO v_survey_exists 
    FROM stand_survey 
    WHERE stand_number = :NEW.stand_number AND survey_status = 1;
    
    IF v_survey_exists = 0 THEN
        raise_application_error(-20001, 'Business Rule Violation: A stand must have an active survey status verified before subdividing.');
    END IF;

    -- Fetch stand size
    SELECT size_m2 INTO v_parent_size FROM stands WHERE stand_number = :NEW.stand_number;
    
    -- Sum existing subdivision sizes
    SELECT COALESCE(SUM(size_m2), 0) INTO v_current_sum 
    FROM stand_subdivisions 
    WHERE stand_number = :NEW.stand_number;
    
    IF (v_current_sum + :NEW.size_m2) > v_parent_size THEN
        raise_application_error(-20002, 'Business Rule Violation: Total subdivisions size exceeds parent stand capacity.');
    END IF;
END;
/

-- Trigger 3: Update Allocation Status AFTER INSERT/DELETE
CREATE OR REPLACE TRIGGER trg_allocation_status_ins
AFTER INSERT ON stand_allocations
FOR EACH ROW
BEGIN
    UPDATE stand_subdivisions 
    SET allocation_status = 1 
    WHERE subdivision_id = :NEW.subdivision_id;
END;
/

CREATE OR REPLACE TRIGGER trg_allocation_status_del
AFTER DELETE ON stand_allocations
FOR EACH ROW
BEGIN
    UPDATE stand_subdivisions 
    SET allocation_status = 0 
    WHERE subdivision_id = :OLD.subdivision_id;
END;
/


