DECLARE
    PROCEDURE drop_table(p_table IN VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE 'DROP TABLE ' || p_table || ' CASCADE CONSTRAINTS';
    EXCEPTION
        WHEN OTHERS THEN NULL;
    END;
    PROCEDURE drop_view(p_view IN VARCHAR2) IS
    BEGIN
        EXECUTE IMMEDIATE 'DROP VIEW ' || p_view;
    EXCEPTION
        WHEN OTHERS THEN NULL;
    END;
BEGIN
    drop_table('stand_allocations_audit');
    drop_table('stand_allocations');
    drop_table('dependents');
    drop_table('stand_owners');
    drop_table('stand_subdivisions');
    drop_table('stand_survey');
    drop_table('stands');
    drop_table('metadata_catalogue');
    
